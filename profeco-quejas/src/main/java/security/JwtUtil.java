package security;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;
import io.jsonwebtoken.security.Keys;

import java.security.Key;
import java.util.Date;

/**
 *
 * @author Carlo
 */
public class JwtUtil {

    // Clave secreta para firmar tokens (en producción debe ir en variables de entorno)
private static final String SECRET = "MI_SUPER_CLAVE_SECRETA_DE_256_BITS_QUE_NO_CAMBIA";
private static final Key SECRET_KEY = Keys.hmacShaKeyFor(SECRET.getBytes());

    // Duración del token: 2 horas
    private static final long EXPIRATION_TIME = 2 * 60 * 60 * 1000;

    public static String generarToken(String username, String rol) {
        return Jwts.builder()
                .setSubject(username)
                .claim("rol", rol)
                .setIssuedAt(new Date())
                .setExpiration(new Date(System.currentTimeMillis() + EXPIRATION_TIME))
                .signWith(SECRET_KEY)
                .compact();
    }

    public static String obtenerRol(String token) {
        return (String) Jwts.parserBuilder()
                .setSigningKey(SECRET_KEY)
                .build()
                .parseClaimsJws(token)
                .getBody()
                .get("rol");
    }

    public static String obtenerUsuario(String token) {
        return Jwts.parserBuilder()
                .setSigningKey(SECRET_KEY)
                .build()
                .parseClaimsJws(token)
                .getBody()
                .getSubject();
    }

    public static boolean validarToken(String token) {
        try {
            Jwts.parserBuilder().setSigningKey(SECRET_KEY).build().parseClaimsJws(token);
            return true;
        } catch (Exception e) {
            return false;
        }
    }
}
